$("#icono_menu").on("click",function(){
    $("#menu_movil").show();
    $("#icono_menu").hide();
});

$("#icono_cerrar").on("click",function(){
    $("#menu_movil").hide();
    $("#icono_menu").show();
});
$("#lista_menu").on("click",function(){
    $("#menu_movil").hide();
    $("#icono_menu").show();
});